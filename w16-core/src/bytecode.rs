// w16-core\src\bytecode.rs
//! # Единый байт-код для runtime-а W16
//!
//! Используем регистровый формат инструкций.
//!
//! ## Технические характеристики
//! - **Размер инструкции:** Фиксированные 32 бита (4 байта).
//! - **Кэш-оптимизация:** Плотная упаковка для максимального попадания в L1i кэш (32KB-64KB).
//! - **Порядок байтов:** Little-Endian (стандарт для x86_64).
//! - **Alignment:** Инструкции должны быть выровнены по 4-байтовой границе для атомарного чтения.
//!
//! ## Формат инструкции (Layout)
//! ```txt
//! | 8 бит  | 8 бит  | 8 бит  | 8 бит  |
//! | OpCode | Reg A  | Reg B  | Reg C  |
//! ```
//! - **OpCode**: Код операции.
//! - **Reg A**: Целевой регистр (Destination) или результат.
//! - **Reg B**: Первый операнд (Source 1).
//! - **Reg C**: Второй операнд (Source 2) или 8-битное константное значение (Immediate).
//!
//! ## Регистровая модель
//! - Доступно 256 виртуальных регистров (`0x00` - `0xFF`) на один кадр стека (Stack Frame).
//! - Регистры имеют размерность 64 бита (8 байт).

/// Код операции(OpCode)
#[repr(u8)]
#[derive(Debug, Clone, Copy, PartialEq, Eq)]
pub enum OpCode {
    /// 0x00: Полная остановка runtime-а.
    Halt = 0x00,

    /// 0x01: Ничего не делать. Серьёзно.
    //
    /// Нужно для выравнивания пустых слотов.
    NoOp = 0x01,

    // ========================================
    // Работа с данными             0x10 - 0x20
    //
    /// 0x10: Копирование: Reg A = Reg B.
    Mov = 0x10,

    /// 0x11: Загрузка 8 бит: Reg A = Imm.
    Load8 = 0x11,

    /// 0x12: Загрузка 16 бит: Reg A = Imm16.
    Load16 = 0x12,

    /// 0x13: Загрузка значения из пула констант (Constant Pool): Reg A = Constants[Index16].
    ///
    /// Почему здесь нет Load32 и Load64?
    /// Для максимальной скорости исполнения в W16 мы выбрали фиксированный размер инструкции — 4 байта.
    /// Поскольку Imm32 требует 4 байта, а Imm64 — 8 байтов, они физически не помещаются
    /// в 32-битную инструкцию вместе с OpCode и адресом регистра.
    ///
    /// Решение: Большие числа хранятся в отдельном массиве в памяти, а в коде
    /// передается только их индекс.
    ///
    /// Используется для загрузки:
    /// 1. Больших чисел (i32, f64 и т.д.)
    /// 2. Указателей на строки (Strings)
    /// 3. Ссылок на объекты/массивы
    LoadConst = 0x13,

    // --- Чтение из памяти (Load) ---
    /// Load8: Reg A = [Reg B] (читать 1 байт, остальное в регистре занулить)
    Ld8 = 0x14,
    /// Load16: Reg A = [Reg B] (читать 2 байта)
    Ld16 = 0x15,
    /// Load32: Reg A = [Reg B] (читать 4 байта)
    Ld32 = 0x16,
    /// Load64: Reg A = [Reg B] (читать 8 байт)
    Ld64 = 0x17,

    // --- Запись в память (Store) ---
    /// Store8: [Reg A] = младший байт Reg B
    St8 = 0x18,
    /// Store16: [Reg A] = младшие 2 байт Reg B
    St16 = 0x19,
    /// Store32: [Reg A] = младшие 4 байта Reg B
    St32 = 0x1A,
    /// Store64: [Reg A] = все 8 байт Reg B
    St64 = 0x1D,
    // ========================================
    // Арифметика для целочисленных 0x20 - 0x30
    //
    /// 0x20: Сложение: Reg A = Reg B + Reg C.
    Add = 0x20,

    /// 0x21: Вычитание: Reg A = Reg B - Reg C.
    Sub = 0x21,

    /// 0x22: Умножение: Reg A = Reg B * Reg C.
    Mul = 0x22,

    /// Деление: Reg A = Reg B / Reg C.
    UDiv = 0x23, // 0x23: Деление беззнаковых целочисленных.
    IDiv = 0x24, // 0x24: Деление знаковых целочисленных.

    /// 0x25: Остаток от деления беззнаковых целочисленных: Reg A = Reg B % Reg C.
    URem = 0x25,

    /// 0x26: Остаток от деления беззнаковых целочисленных: Reg A = Reg B % Reg C.
    IRem = 0x26,

    // ========================================
    // Арифметика для дробных чисел 0x27 - 0x31
    //
    FAdd = 0x27,
    FSub = 0x28,
    FMul = 0x29,
    FDiv = 0x30,
    FRem = 0x31,

    // ========================================
    // Дополнительная арифметика    0x32 - 0x3F
    //
    /// 0x32: Унарный минус (целое): Reg A = -Reg B.
    /// Работает по принципу дополнительного кода (invert + 1).
    Neg = 0x32,

    /// 0x33: Унарный минус (дробное): Reg A = -Reg B.
    /// Просто инвертирует старший (знаковый) бит f64.
    FNeg = 0x33,

    /// 0x34: Абсолютное значение f64.
    FAbs = 0x34,

    // ========================================
    // Логика и сравнения (integer) 0x40 - 0x4F
    //
    IEq = 0x40,  // ==
    INe = 0x41,  // !=
    ISLt = 0x42, // <  (signed)
    ISLe = 0x43, // <= (signed)
    ISGt = 0x44, // >  (signed)
    ISGe = 0x45, // >= (signed)
    IULt = 0x46, // <  (unsigned)
    IULe = 0x47, // <= (unsigned)
    IUGt = 0x48, // >  (unsigned)
    IUGe = 0x49, // >= (unsigned)

    // ========================================
    // Логика и сравнения (float) 0x50 - 0x5F
    //
    FEq = 0x50,
    FNe = 0x51,
    FLt = 0x52,
    FLe = 0x53,
    FGt = 0x54,
    FGe = 0x55,
    /// 0x5F: Выбор: Reg A = (Reg A != 0) ? Reg B : Reg C.
    Select = 0x5F,

    // ========================================
    // Битовые операции             0x60 - 0x67
    //
    /// Reg A = Reg B & Reg C (И)
    And = 0x60,
    /// Reg A = Reg B | Reg C (ИЛИ)
    Or = 0x61,
    /// Reg A = Reg B ^ Reg C (Исключающее ИЛИ)
    Xor = 0x62,
    /// Reg A = !Reg B (Инверсия)
    Not = 0x63,
    /// Reg A = Reg B << Reg C (Сдвиг влево)
    Shl = 0x64,
    /// Reg A = Reg B >> Reg C (Логический сдвиг вправо, для unsigned)
    Shr = 0x65,
    /// Reg A = Reg B >> Reg C (Арифметический сдвиг, сохраняет знак)
    Sar = 0x66,

    // ========================================
    // Управление потоком (flow)    0x67 - 0x70
    //
    /// 0x50: Прыжок по адресу из Reg A
    Jmp = 0x67,
    /// 0x51: Прыжок если Reg A != 0
    Jnz = 0x68,
    /// 0x52: Вызов функции
    Call = 0x69,
    /// 0x53: Возврат
    Ret = 0x70,
    /// 0x72: Прыжок если Reg A == 0.
    Jz = 0x72,

    // ========================================
    // Приведение типов (Casting)   0x80 - 0x8F
    //
    F2I = 0x80, // f64 -> i64
    I2F = 0x81, // i64 -> f64
    F2U = 0x82, // f64 -> u64
    U2F = 0x83, // u64 -> f64

    // ========================================
    // I/O                          0x90 - 0x93
    PrintStr = 0x90,
    PrintInt = 0x91,
    PrintUInt = 0x92,
    PrintFloat = 0x93,
}

/// Декодированная инструкция W16.
/// Мы разбираем сырые 32 бита на логические части для быстрого доступа.
#[derive(Debug, Clone, Copy, PartialEq, Eq)]
pub struct Instruction {
    /// Что делать (Код операции)
    pub opcode: OpCode,

    /// Регистр назначения (куда пишем результат)
    pub a: u8,

    /// Первый операнд (обычно регистр-источник)
    pub b: u8,

    /// Второй операнд (регистр или 8-битное число)
    pub c: u8,
}

impl Instruction {
    /// Создает декодированную инструкцию из сырого u32.
    /// Это "сердце" нашего декодера.
    #[inline(always)]
    pub fn decode(raw: u32) -> Self {
        let op_byte = (raw & 0xFF) as u8;
        // Вместо transmute используем безопасное приведение
        let opcode = unsafe { std::mem::transmute::<u8, OpCode>(op_byte) };

        Self {
            opcode,
            a: ((raw >> 8) & 0xFF) as u8,
            b: ((raw >> 16) & 0xFF) as u8,
            c: ((raw >> 24) & 0xFF) as u8,
        }
    }

    /// Вспомогательный метод для получения 16-битного значения (комбинирует b и c).
    /// Используется для Load16 и LoadConst.
    #[inline(always)]
    pub fn imm16(&self) -> u16 {
        // Склеиваем байты B и C обратно в u16
        ((self.c as u16) << 8) | (self.b as u16)
    }
}

pub struct ConstantPool {
    /// Линейное хранилище всех констант (f64, i64, string data)
    pub data: Vec<u8>,
}

impl ConstantPool {
    pub fn new() -> Self {
        Self { data: Vec::new() }
    }

    /// Получить 64-битное значение по индексу.
    /// Используется для быстрой загрузки f64 или i64.
    #[inline(always)]
    pub fn get_u64(&self, index: usize) -> u64 {
        // Используем slice и преобразование в массив для скорости.
        // В runtime мы предполагаем, что индекс валиден (проверено компилятором).
        let bytes = &self.data[index..index + 8];
        u64::from_le_bytes(bytes.try_into().unwrap())
    }

    /// Получить срез байтов (например, для строк).
    /// В W16 строка в ConstantPool обычно представлена как [len: u64][data: bytes].
    #[inline(always)]
    pub fn get_slice(&self, index: usize, len: usize) -> &[u8] {
        &self.data[index..index + len]
    }

    /// Добавляет строку в пул и возвращает индекс начала данных.
    /// Формат: [u64 len][bytes...]
    pub fn add_string(&mut self, s: &str) -> usize {
        let start_index = self.data.len();
        let len = s.len() as u64;

        // Записываем длину (8 байт, Little Endian)
        self.data.extend_from_slice(&len.to_le_bytes());
        // Записываем само тело строки
        self.data.extend_from_slice(s.as_bytes());

        start_index
    }
}

/// Полный набор данных для исполнения Виртуальной Машиной.
/// Объединяет инструкции и константы в единый контекст.
pub struct Bytecode {
    /// Список инструкций (программа).
    /// Используем Vec для простоты, в рантайме это будет линейный массив в памяти.
    pub instructions: Vec<Instruction>,

    /// Пул констант для хранения тяжелых данных (строки, f64, i64).
    pub constant_pool: ConstantPool,
}

impl Bytecode {
    pub fn new(instructions: Vec<Instruction>, constant_pool: ConstantPool) -> Self {
        Self {
            instructions,
            constant_pool,
        }
    }

    /// Быстрый доступ к инструкции по индексу (Instruction Pointer).
    /// В горячем цикле VM мы будем использовать этот метод.
    #[inline(always)]
    pub fn get_instruction(&self, ip: usize) -> Option<&Instruction> {
        self.instructions.get(ip)
    }
}
