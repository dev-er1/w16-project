//! # Модуль lexer.
//!
//! Лексер отвечает только за механическое разбиение текстового IR на токены:
//! ключевые слова, имена с префиксами (`@`, `$`, `^`), литералы, операторы и
//! пунктуацию. Он не знает о грамматике функций или типах — этим занимается
//! parser и semantic verifier.

mod scanner;
mod token;

/// Ошибка лексического анализа.
pub use scanner::{LexError, Lexer};
/// Токены и spans исходного текста.
pub use token::{Span, Token, TokenKind};
